#ifndef PIN_DEFINITIONS_H
#define PIN_DEFINITIONS_H	1

#define ON_OFF					  (1<<0) 	//P0
#define GPS_INT					  (1<<10) //P1
#define ACC_INT						(1<<9)	//P0
#define GYRO_INT1					(1<<11)	//P0
#define MAGNO_INT					(1<<19)	//P0
#define RGB_CONTROL				(1<<0)	//P1
#define RGB_GREEN					(1<<1)	//P1
#define RGB_RED						(1<<4)	//P1
#define RGB_BLUE					(1<<8)	//P1
#define ON_OFF_LED				(1<<6)	//P2
#define USB_CONNECT				(1<<9)	//P2


#endif //#ifndef PIN_DEFINITIONS_H
